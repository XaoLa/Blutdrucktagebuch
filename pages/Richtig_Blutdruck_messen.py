import streamlit as st
st.title('Richtig Blutdruck messen')
st.subheader('Vorbereitung')
multi = ''' - **Ruhepause einlegen:** Vor der Messung sollte man mindestens 5 Minuten in Ruhe sitzen, um den Kreislauf zu beruhigen.  
- **Körperhaltung:** Setzen Sie sich bequem hin, mit dem Rücken an eine Lehne gelehnt. Die Füße sollten flach auf dem Boden stehen und die Beine nicht überkreuzt sein.  
- **Armposition:** Der Arm sollte auf Herzhöhe gelagert sein. Am besten legt man den Arm auf einen Tisch, um ihn zu stützen.
'''
st.markdown(multi)

st.subheader('Vorbereitung')
multi = ''' - **Manschette anlegen:** Legen Sie die Manschette des Blutdruckmessgeräts etwa 2–3 cm über dem linken Ellbogen um den Oberarm. Die Manschette sollte eng anliegen, aber nicht einschnüren.  
- **Messgerät starten:** Schalten Sie das Blutdruckmessgerät ein und starten Sie die Messung gemäß den Anweisungen des Herstellers.  
- **Stillhalten:** Während der Messung sollten Sie ruhig sitzen und nicht sprechen oder sich bewegen.  
- **Werte ablesen:** Nach der Messung zeigt das Gerät den systolischen (oberen) und diastolischen (unteren) Blutdruckwert sowie die Pulsfrequenz an.
'''
st.markdown(multi)

st.subheader('Wichtige Hinweise')
multi = ''' - **Regelmässigkeit:** Messen Sie den Blutdruck möglichst immer zur gleichen Tageszeit, um vergleichbare Werte zu erhalten.  
- **Protokoll führen:** Notieren Sie die gemessenen Werte in einem Blutdruckpass oder einer App, um Veränderungen über die Zeit zu verfolgen.  
- **Mehrfachmessung:** Führen Sie bei Unsicherheit oder untypischen Werten eine zweite Messung nach einigen Minuten durch und verwenden Sie den Durchschnittswert.  
- **Keine Aufregung:** Vermeiden Sie vor der Messung körperliche Anstrengungen, Koffein, Alkohol und Nikotin, da diese den Blutdruck beeinflussen können.
'''
st.markdown(multi)






